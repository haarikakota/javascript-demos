function hoursAttended(attended,length){
    if(typeof(attended) == 'string' && attended.trim()!= ""){
       
        attended = Number(attended);
        }
    
if(typeof(length) == 'string' && length.trim()!= ""){

   length = Number(length);
   }

 if( typeof(attended) == "number" && attended >= 0 &&  typeof(length) == 'number' && length >=0 && Number.isInteger(attended) && Number.isInteger(length) && attended <= length){
       return true;
   }


   else{
       return false;
   }
}
